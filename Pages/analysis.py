# Carregar os dados
df = pd.read_excel('https://github.com/MichaelJourdain93/Datathon_Passos_Magicos/raw/main/Datasets/dt_curated_passos_magicos.xlsx', engine='openpyxl')

# T√≠tulo da P√°gina
st.title("üìä An√°lise dos Dados da ONG Passos M√°gicos")

